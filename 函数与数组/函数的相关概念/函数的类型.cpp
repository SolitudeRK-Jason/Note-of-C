# include <stdio.h>

int f()
{
	return 10.5;
}
int main(void)
{
	int i = 1;
	double x = 6.6;

	i = f();
	x = f();

	printf("i = %d\n", i);
	printf("x = %lf\n", x);
	return 0;
}
/*
在visio studio中输出结果为
-------------------------
i= 10
x = 10.000000
-------------------------
总结：函数返回值的类型也称函数类型，若函数名前的返回值类型与return表达式;中表达式类型不同，则最终
		函数返回值类型以函数名前的类型为准
*/